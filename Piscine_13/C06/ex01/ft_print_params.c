#include <unistd.h>

int	main(int ac, char **av)
{
	short	i;
	short	j;

	i = 0;
	j = 0;
	while (av[++i] && ac)
	{
		while (av[i][j])
		{
			write(1, &av[i][j++], 1);
		}
		j = 0;
		write(1, "\n", 1);
	}
	return (0);
}
