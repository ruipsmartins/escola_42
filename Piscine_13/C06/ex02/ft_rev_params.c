#include <unistd.h>

int	main(int ac, char **av)
{
	short	i;
	short	j;

	i = ac;
	while (i-- > 1)
	{
		j = 0;
		while (av[i][j])
		{
			write(1, &av[i][j++], 1);
		}
		write(1, "\n", 1);
	}
	return (0);
}
