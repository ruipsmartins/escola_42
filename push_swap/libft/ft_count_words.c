#include "libft.h"

int	ft_count_words(char const *str, char c)
{
	int		count;
	int		i;

	if (ft_strlen(str) == 0)
		return (0);
	count = 1;
	i = 0;
	while (str[i] == c)
		i++;
	while (str[i])
	{
		if (str[i] == c && str[i + 1] != c && str[i + 1] != '\0')
			count++;
		i++;
	}
	return (count);
}